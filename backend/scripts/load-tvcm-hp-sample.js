async function main() {
  console.log('🦷 スマイル歯科 TVCM・公式HP サンプルデータ投入開始...')
  console.log(`📝 対象: ${PATIENTS.length}名`)

  const client = await pool.connect()
  try {
    await client.query('BEGIN')

    let inserted = 0

    for (const [name, name_kana, phone, age_group, postal_code, referral_source, gender] of PATIENTS) {
      await client.query(
        `INSERT INTO patients (name, name_kana, phone, age_group, postal_code, referral_source, gender, is_active)
         VALUES ($1,$2,$3,$4,$5,$6,$7,TRUE)`,
        [name, name_kana, phone, age_group, postal_code, referral_source, gender]
      )
      inserted++
      process.stdout.write(`✅ ${name} (${referral_source})\n`)
    }

    await client.query('COMMIT')

    // 確認
    const check = await client.query(`
      SELECT referral_source, COUNT(*) AS 患者数
      FROM patients
      WHERE referral_source IN ('TVCM', '公式HP')
      GROUP BY referral_source ORDER BY referral_source
    `)

    console.log('\n════════════════════════════════')
    console.log(`✅ 新規登録: ${inserted}名`)
    console.log('════════════════════════════════')
    console.log('\n📊 DB確認:')
    console.table(check.rows)
    console.log('🎉 完了！')

  } catch (err) {
    await client.query('ROLLBACK')
    console.error('❌ エラー（ロールバック）:', err.message)
    process.exit(1)
  } finally {
    client.release()
    await pool.end()
  }
}

main()
